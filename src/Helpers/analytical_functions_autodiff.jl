J(m::ForwardMap2D,x) = _evaluate_forward_jacobian_2d(m.panel,m.radius,x)
J(m::ForwardMap3D,x) = _evaluate_forward_jacobian_3d(m.panel,m.radius,m.thickness,x)
Jt(m::ForwardMap2Dor3D,x) = transpose(J(m,x))
metric(m::ForwardMap2Dor3D,x) = Jt(m,x)⋅J(m,x)
inv_metric(m::ForwardMap2Dor3D,x) = inv(metric(m,x))
detg(m::ForwardMap2Dor3D,x)  = det(metric(m,x))
sqrtg(m::ForwardMap2Dor3D,x)  = sqrt(detg(m,x))
forward_jacobian(m::ForwardMap2Dor3D,x) = J(m,x)
forward_pinv_jacobian(m::ForwardMap2Dor3D,x) = pinvJ(J(m,x))
